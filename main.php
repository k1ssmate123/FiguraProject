   <div class="row p-3">
       <div class="col-md-6 description">
           <p><b>A gyönyörű Somogyi-dombság közepén helyezkedik el Tab, itt találhatod meg a Figura Bisztrót, ahol 2013 novembere óta várjuk vendégeinket fiatalos, jó kedvű személyzettel.</b></p><br>

           <p>A Figura felvállalt célja, hogy széles rétegekhez, illetve minden korosztályhoz eljuttassa a gasztronómia legkiemelkedőbb értékeit, illetve színes programválasztékkal szórakoztassa a vendégeket barátságos és letisztult környezetben. Ételeink kizárólag a legkiválóbb szezonális alapanyagokból készülnek, így kimagaslóan finom és választékos ajánlatokkal várjuk kedves vendégeinket. Sportkedvelő vendégeink részére is megfelelő helyet biztosítunk a szurkoláshoz, melyre nagy kivetítőnk ad lehetőséget.</p>
